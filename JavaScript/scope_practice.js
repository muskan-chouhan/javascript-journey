function shoppingCart(...items) {
    // Step 1: total nikalna
    let sum = items.reduce((acc, curr) => acc + curr, 0);
    console.log("Total before discount:", sum);

    // Step 2: discount lagana agar > 500 hai
    if (sum > 500) {
        let discountPercent = 10; // 10% discount
        let discountAmount = sum * (discountPercent / 100);
        let finalPrice = sum - discountAmount;
        console.log("After discount:", finalPrice);
    } else {
        console.log("No discount applied");
    }
}

// Test
shoppingCart(200, 300, 4000);
shoppingCart(100, 50);



