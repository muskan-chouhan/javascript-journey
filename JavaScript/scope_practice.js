function loginSystem(username, password) {
    const dbUser = "muskan";   // database me saved user
    const dbPass = "1234";

    function greetUser() {
        console.log(`Welcome back, ${username}! 🎉`);
    }

    if (username === dbUser && password === dbPass) {
        greetUser();  // ✅ inner function call hua
    } else {
        console.log("❌ Invalid login details");
    }
}

// test cases
loginSystem("muskan", "1234");  // ✅ Welcome back, muskan! 🎉
loginSystem("anya", "1111");    // ❌ Invalid login details


